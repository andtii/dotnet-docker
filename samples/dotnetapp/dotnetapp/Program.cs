  private static string GetMessage(string[] message)
  {
    var buffer = new StringBuilder();
    foreach(var m in message)
    {
      if (m == "--with-color") continue;
      buffer.Append(" ");
      buffer.Append(m);
    }

    return buffer.ToString();
  }

